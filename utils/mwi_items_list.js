// Helper: Get item name from HRID
function getItemName(hrid) {
  return MWI_ITEMS[hrid] || null;
}

// Helper: Get item HRID from name
function getItemHrid(name) {
  return Object.keys(MWI_ITEMS).find(key => MWI_ITEMS[key] === name) || null;
}

// Helper: Search items by partial name
function searchItems(query) {
  const lowerQuery = query.toLowerCase();
  return Object.entries(MWI_ITEMS)
    .filter(([hrid, name]) => name.toLowerCase().includes(lowerQuery))
    .map(([hrid, name]) => ({ hrid, name }));
}

